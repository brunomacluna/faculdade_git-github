public class Ex6 {
	
	public void imparesEntre100e200() {
					
		for (int i = 100; i <= 200; i++) {
			
			if (i % 2 != 0) {
				System.out.print(i+" ");// se o resto de divisão for diferete de zero é impar
			}//fim if
			
		}//fim for
		
	}//fim metodo imparesEntre100e200
	
}//fim class Ex6
